package bean;

public class LoginBean {
	private String uname, pass;

	public String getUsername() {
		return uname;
	}

	public void setUsername(String uname) {
		this.uname = uname;
	}

	public String getPassword() {
		return pass;
	}

	public void setPassword(String pass) {
		this.pass = pass;
	}

}
